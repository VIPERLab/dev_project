package cn.kuwo.sing.bean;

import java.io.Serializable;

import com.j256.ormlite.field.DatabaseField;

public class Music extends MySong implements Serializable{
	private static final long serialVersionUID = -7527661841970126103L;
	
	@DatabaseField
	private String sign;
	@DatabaseField
	private long progress;
	@DatabaseField
	private long total;
	@DatabaseField
	private long originalTotal;
	@DatabaseField
	private long accompTotal;
	@DatabaseField
	private String name;
	@DatabaseField
	private String artist;
	@DatabaseField
	private String album;
	@DatabaseField
	private long duration;
	@DatabaseField
	private String format;
	@DatabaseField
	private int hot;
	@DatabaseField
	private String img;
	@DatabaseField
	private boolean hasSing;
	
	@DatabaseField
	private String res;
	
	@DatabaseField
	private String child;
	
	@DatabaseField
	private String desc;
	
	@DatabaseField
	private int digest;
	
	@DatabaseField
	private int num;

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	public long getProgress() {
		return progress;
	}

	public void setProgress(long progress) {
		this.progress = progress;
	}

	public long getTotal() {
		return total;
	}

	public void setTotal(long total) {
		this.total = total;
	}

	public long getOriginalTotal() {
		return originalTotal;
	}

	public void setOriginalTotal(long originalTotal) {
		this.originalTotal = originalTotal;
	}

	public long getAccompTotal() {
		return accompTotal;
	}

	public void setAccompTotal(long accompTotal) {
		this.accompTotal = accompTotal;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getArtist() {
		return artist;
	}

	public void setArtist(String artist) {
		this.artist = artist;
	}

	public String getAlbum() {
		return album;
	}

	public void setAlbum(String album) {
		this.album = album;
	}

	public long getDuration() {
		return duration;
	}

	public void setDuration(long duration) {
		this.duration = duration;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public int getHot() {
		return hot;
	}

	public void setHot(int hot) {
		this.hot = hot;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public boolean isHasSing() {
		return hasSing;
	}

	public void setHasSing(boolean hasSing) {
		this.hasSing = hasSing;
	}

	public String getRes() {
		return res;
	}

	public void setRes(String res) {
		this.res = res;
	}

	public String getChild() {
		return child;
	}

	public void setChild(String child) {
		this.child = child;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public int getDigest() {
		return digest;
	}

	public void setDigest(int digest) {
		this.digest = digest;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

}
